document.getElementById('depreciationRate').addEventListener('input', function() {
    const id = this.value.trim();
    const form = document.getElementById('abschreibungForm2');

    if (equipmentList[id]) {
        const equipment = equipmentList[id];
        form.querySelector('#itemName').value = equipment.name;
        form.querySelector('#cost').value = equipment.itemCosts.toFixed(2).replace('.', ','); // german decimal comma
        form.querySelector('#depreciationRateYears').value = equipment.depreciationMonths;
        
        // Calculate yearly depreciation (cost / years in years)
        const depreciationYear = equipment.itemCosts / (equipment.depreciationMonths / 12);
        form.querySelector('#depreciationRateYear').value = depreciationYear.toFixed(2).replace('.', ',');

        // Calculate monthly depreciation (/1632)
        const depreciationMonth = depreciationYear / 1632;
        form.querySelector('#depreciationRateMonth').value = depreciationMonth.toFixed(2).replace('.', ',');

    } else {
        // Clear fields if ID not found
        form.querySelector('#itemName').value = '';
        form.querySelector('#cost').value = '';
        form.querySelector('#depreciationRateYears').value = '';
        form.querySelector('#depreciationRateYear').value = '';
        form.querySelector('#depreciationRateMonth').value = '';
    }
});